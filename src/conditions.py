# conditions.py
# Базовые наборы фраз для распознавания намерений пользователя
GREETINGS = ["привет", "здравствуй", "добрый день", "хай", "приветик", "приветствую", "здравствуйте", "доброе утро", "добрый вечер", "доброй ночи"]
FAREWELLS = ["пока", "до свидания", "прощай", "до встречи", "бб", "увидимся", "до скорого", "до завтра", "до следующей встречи", "до скорой встречи"]

# Словарь стандартных вопросов (FAQ) и ответов для бота
FAQ = {
    "что ты умеешь?": "Я — твой персональный чат-бот. Пока я умею только здороваться, прощаться и отвечать на базовые вопросы, но мой код постоянно дорабатывается!",
    "как тебя зовут?": "Меня зовут Джун-Бот. Я помогаю Александру осваивать Python и Flask.",
    "кто твой создатель?": "Меня создали два Джуна под присмотром строгого Сеньора ChatGPT.",
    "какой стек технологий?": "Я работаю на Flask, Python и немного магии JavaScript.",
    "как изменить имя?": "Просто напиши 'сменить имя на [твоё имя]', и я буду называть тебя так!",
    "как сменить язык?": "Напиши 'сменить язык на [язык]', и я постараюсь переключиться. (Пока в разработке)"
}

def get_bot_response(user_message):
    # Приводим сообщение к нижнему регистру и убираем лишние пробелы для удобства сравнения
    text = user_message.lower().strip()
    
    # Проверяем, есть ли приветствие в сообщении
    if text in GREETINGS:
        return "Привет-привет! Чем могу помочь?"
    
    # Проверяем, есть ли прощание в сообщении
    if text in FAREWELLS:
        return "До связи! Буду ждать тебя здесь."
    
    # Проверяем, есть ли ответ в FAQ
    faq_answer = FAQ.get(text)
    if faq_answer:
        return faq_answer

    # 5. Дефолтный ответ, если ничего не подошло
    return None  # Возвращаем None, чтобы main.py мог обработать это и дать стандартный ответ

def check_commands(text):
    """Проверяет, является ли текст командой смены настроек."""
    text = text.lower().strip()
    
    # Смена имени: "сменить имя на Александр"
    if text.startswith("сменить имя на "):
        new_name = text.replace("сменить имя на ", "").strip().capitalize()
        return {"user_name": new_name}, f"Приятно познакомиться, {new_name}! Теперь я буду называть тебя так."
    
    # Смена языка (пока просто демо)
    if text.startswith("сменить язык на "):
        new_lang = text.replace("сменить язык на ", "").strip()
        return {"language": new_lang}, f"Язык изменен на {new_lang}. (В разработке)"

    return None, None